def proteins(strand):
    codon_to_amino_acid = {
    'AUG': 'Methionine',
    'UUU': 'Phenylalanine',
    'UUC': 'Phenylalanine',
    'UUA': 'Leucine',
    'UUG': 'Leucine',
    'UCU': 'Serine',
    'UCC': 'Serine',
    'UCA': 'Serine',
    'UCG': 'Serine',
    'UAU': 'Tyrosine',
    'UAC': 'Tyrosine',
    'UGU': 'Cysteine',
    'UGC': 'Cysteine',
    'UGG': 'Tryptophan',
    'UAA': 'STOP',
    'UAG': 'STOP',
    'UGA': 'STOP'
    }

    translation = []
    for i in range(len(strand)//3):
        if codon_to_amino_acid[strand[:3]] == 'STOP':
            return translation
        translation.append(codon_to_amino_acid[strand[:3]])
        strand = strand[3:]
    return translation

